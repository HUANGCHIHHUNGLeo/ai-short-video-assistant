// 通用 CTA 原則（適用於所有類型）
export const CTA_GENERAL_PRINCIPLES = `## CTA 通用原則

### CTA 有效的前提：內容要有價值！
⚠️ 如果內容沒乾貨、沒價值，CTA 再好也沒用！
觀眾會追蹤/留言/收藏，是因為「這個內容對我有用」或「我有話想說」

### ____ 要怎麼填？
根據創作者的產業和影片主題來填，例如：
- 工程師：「你 debug 最久的一次是多久？」
- 餐飲業：「你們店最常被客訴什麼？」
- 創業者：「你創業第一年虧了多少？」

### 爛的 CTA（禁止）：
- 「追蹤我吧」「記得按讚」（沒給理由）
- 「留言告訴我你的想法」（太空泛）
- 「分享給朋友」（沒指定誰）
- 「現在就開始改變你的人生吧！」（太雞湯）
- 「記得按讚分享訂閱開啟小鈴鐺」（太公式化）

### ⚠️ CTA 寫法規則
- CTA 不能照搬用戶選擇的類型原封不動！要口語化、跟前一段內容自然銜接
- CTA 要跟前面腳本的故事/主題有關聯，不能突然冒出一句不相干的話
- 例如用戶選「分享 / 轉發給朋友」→ 不要寫「轉發給朋友」，要寫成跟內容相關的口語句子：
  「把這支影片丟給也在煩這件事的朋友吧」
  「分享給你身邊那個也想創業的人看看」
- CTA 後面加上「（可選）」兩個字，讓觀眾感覺不是被強迫
- 語氣要輕鬆自然，像朋友隨口講的，不是業務在推銷`

/**
 * 根據 CTA 類型取得對應的指引
 * @param ctaType - CTA 類型
 * @returns CTA 指引文字
 */
export function getCTAGuide(ctaType: CTAType): string {
  return CTA_GUIDES[ctaType] || CTA_GUIDES.follow
}
